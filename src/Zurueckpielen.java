    private void btnZurueckpielenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnZurueckpielenActionPerformed
        Zippen zip = new Zippen();
        String directory;
        
        try {
            // Ziel w√§len
            JFileChooser destination = new JFileChooser();
            destination.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            destination.setDialogTitle("Zielauswahl");
            destination.showOpenDialog(null);
            directory = destination.getSelectedFile().getPath();
            
            if (!directory.isEmpty())
            {
                // entpacken.extractArchive();
                List<String> listeob = listFiles.getSelectedValuesList();
                listeob.toArray(new String[listeob.size()]);
                zip.extractArchive(strZipfile, listeob, directory);
            }
        } catch(Exception e) {
            System.out.println("Fehler beim entpacken!");
        }
    }//GEN-LAST:event_btnZurueckpielenActionPerformed

    private void listFilesValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listFilesValueChanged
        if (listFiles.getSelectedIndex() == -1 )
        {
            btnZurueckpielen.setEnabled(false);
        } else {
            btnZurueckpielen.setEnabled(true);
        }
    }//GEN-LAST:event_listFilesValueChanged

    public void ZeigeZipInhalt()
    {
        Zippen zip = new Zippen();
        
        try {
            String[] str = zip.leseZipinhalt(strZipfile);

            DefaultListModel model = new DefaultListModel();
            
            
            for (int x=0; x<str.length; x++)
            {
                model.addElement(str[x]);
            }
                        
            listFiles.setModel(model);
        } catch(Exception e) {
            System.out.println("Fehler beim entpacken!");
        }
    }
    
